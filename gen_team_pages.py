# ── Page builder ───────────────────────────────────────────────────────

def build_tags_html(tags, location_tag):
    html = ''
    for t in tags:
        html += f'        <span class="doc-tag">{t}</span>\n'
    html += (f'        <span class="doc-tag doc-tag--location">\n'
             f'          {ICON_PIN}\n'
             f'          {location_tag}\n'
             f'        </span>\n')
    return html

def build_edu_html(education):
    if not education:
        return ''
    items = ''
    for e in education:
        items += f"""          <li class="doc-edu-item">
            <div class="doc-edu-item__icon">{e['icon']}</div>
            <div class="doc-edu-item__body">
              <div class="doc-edu-item__title">{e['title']}</div>
              <div class="doc-edu-item__sub">{e['sub']}</div>
            </div>
          </li>\n"""
    return f"""        <div>
          <p class="doc-section__title">Освіта та досвід</p>
          <ul class="doc-edu-list">
{items}          </ul>
        </div>"""

def build_specialties_html(specialties):
    items = ''.join(f'      <div class="doc-spec-item">{s}</div>\n' for s in specialties)
    cols = min(3, len(specialties))
    return (f'      <div style="display:grid; grid-template-columns: repeat({cols},1fr); '
            f'gap: 10px 40px; max-width: 700px;">\n{items}      </div>')

