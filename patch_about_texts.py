def replace_about_block(html, new_paras):
    """Replace the content inside the doc-about__text / doc-about (solo) block."""
    para_html = '\n'.join(f'        <p>{p}</p>' for p in new_paras)

    # Pattern 1: two-column layout — replace paragraphs inside doc-about__text
    def repl_twocol(m):
        return (m.group(1)
                + '\n        <p class="doc-section__title">Про спеціаліста</p>\n'
                + para_html + '\n      ')

    pattern_twocol = (
        r'(<div class="doc-about__text">)\s*'
        r'<p class="doc-section__title">Про спеціаліста</p>\s*'
        r'(?:<p>.*?</p>\s*)+'
    )
    new_html, n = re.subn(pattern_twocol, repl_twocol, html, flags=re.DOTALL)
    if n:
        return new_html

    # Pattern 2: single-column layout (no education)
    def repl_single(m):
        return (m.group(1)
                + '\n      <p class="doc-section__title">Про спеціаліста</p>\n'
                + para_html + '\n    ')

    pattern_single = (
        r'(<div class="doc-about__text"[^>]*>)\s*'
        r'<p class="doc-section__title">Про спеціаліста</p>\s*'
        r'(?:<p>.*?</p>\s*)+'
    )
    new_html, n = re.subn(pattern_single, repl_single, html, flags=re.DOTALL)
    return new_html


for fname, paras in PATCHES.items():
    path = os.path.join(BASE, fname)
    with open(path, encoding='utf-8') as f:
        html = f.read()

    new_html = replace_about_block(html, paras)

    if new_html == html:
        print(f'!! SKIP (no match): {fname}')
        continue

    with open(path, 'w', encoding='utf-8') as f:
        f.write(new_html)
    print(f'OK  {fname}')

print('\nГотово!')
